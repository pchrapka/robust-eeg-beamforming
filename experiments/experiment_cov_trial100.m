%% experiment_cov_trial100
%   Description
%   -----------
%   Compare the performance between an ensemble average covariance and a
%   trialwise covariance where the input SNR levels are the same and both
%   use 100 trials and 1000 samples
%
%   Issue:
%   Other results are showing different SINR performance for the same SNR
%   levels, which seems strange.
%
%   Hypothesis: 
%   I would expect them to be the same. But that's not the case.

snr_step = 10;

sim_file = 'sim_data_bem_1_100t_1000s_keeptrials_snrpertrial';
source_file = 'src_param_mult_cortical_source_17_lag40';
source_name = 'mult_cort_src_17_lag40';

% spatially uncorrelated
% source_file = 'src_param_mult_cortical_source_18_lag40';
% source_name = 'mult_cort_src_18_lag40';

%% single trial with trial covariance
run_sim_vars_bem_mult_paper_locs2(...
    sim_file, source_file, source_name,...
    ...'snrs',-10:snr_step:30,...
    'snrs',-25:snr_step:15,...
    'cov_type','trial',...
    'cov_samples',[1:1000],...
    'hmconfigs',{'matched'}...
    );

% plot single trial sinr
plot_sinr_mult_config_paper(...
    sim_file, source_name,...
    ...'snrs',-10:snr_step:30,...
    'snrs',-25:snr_step:15,...
    'onaverage',false,...
    'trial_idx',1:100,...
    'datatag','locs2_covtrial_s1-1000',...
    'PlotGroups',{'matched-paper'}...
    );

%% single trial with time covariance
run_sim_vars_bem_mult_paper_locs2(...
    sim_file, source_file, source_name,...
    ...'snrs',-10:snr_step:30,...
    'snrs',-25:snr_step:15,...
    'cov_type','time',...
    'hmconfigs',{'matched'}...
    );

% plot single trial sinr
plot_sinr_mult_config_paper(...
    sim_file, source_name,...
    ...'snrs',-10:snr_step:30,...
    'snrs',-25:snr_step:15,...
    'onaverage',true,...
    'datatag','locs2_covtime',...
    'PlotGroups',{'matched-paper'}...
    );


%% ensemble average
